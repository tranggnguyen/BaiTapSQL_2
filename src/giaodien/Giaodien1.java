    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        Sach s = new Sach();
        s.setMaTaiLieu(matailieuTimText1.getText());
        try {
            s = dks.timkimSach(s);
            if (s != null) {
                df.setNumRows(0);
                df.addRow(new Object[]{//thêm thứ tự từ bản sách
                    s.getMaTaiLieu(),
                    s.getNhaSanXuat(),
                    s.getSoBanPhatHanh(),
                    s.getTenTacGia(),
                    s.getSoTrang()
                });
            } else {
                df.setNumRows(0);
                JOptionPane.showMessageDialog(null, "Khong có mã trong Data");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Giaodien2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton10ActionPerformed

