    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        Sach tapchi = new Sach(matailieuText1.getText(), tennhaxbText1.getText(), sobanphathanhText1.getText(), tentacgiaText1.getText(), sotrangText1.getText());// lấy thông tin r bỏ vào sách
        List<Sach> listSach = new ArrayList<>();//khởi tạo 1 danh sách
        boolean kt = true;// ktra
        try {
            listSach = dks.layDuLieu();//lấy dữ liệu sách
            for (Sach sa : listSach) {

                if (sa.getMaTaiLieu().equals(tapchi.getMaTaiLieu())) {//so sánh chữ

                    JOptionPane.showMessageDialog(null, "Đã có mã tài liệu trong data");//in ra màn hình
                    kt = false;// gán ktr bằng false
                    break;//dừng vòng lặp
                }

            }
            if (kt == true) {
                dks.themSach(tapchi);
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Giaodien1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

